a=int(input("Enter a number"))
Sum=0
for i in range(1,a):
    if(a%i==0):
        print(i,"is a factor ")
        Sum+=i
if(Sum==a):
    print(a,"is a perfect number")
    
else:
    print(a,"is not a perfect number")
    


